import 'package:flutter/services.dart';

import '../data/app_prefs.dart';

/// Тактильный отклик приложения — один словарь на всё.
///
/// Раньше `HapticFeedback` вызывался прямо из экранов, и сила отклика зависела
/// от того, кто писал экран: одно и то же по смыслу действие в одном месте
/// щёлкало, в другом молчало. Здесь смысл действия, а не сила удара.
///
/// **Словарь (не расширять без причины):**
/// * [tap] — человек что-то выбрал: чип, сегмент, день, вкладка. Самый частый
///   и самый тихий отклик;
/// * [commit] — действие состоялось: запись сохранена, вложение добавлено,
///   привычка отмечена;
/// * [celebrate] — то, чем можно гордиться: серия продлена, запись завершена;
/// * [warn] — потеря: удаление, отмена, ошибка.
///
/// Вибрацию можно выключить целиком в настройках: она нравится не всем, а
/// дневник читают в тишине.
class Haptics {
  const Haptics._();

  static bool get _on => AppPrefs.instance.haptics;

  /// Выбор: чип, сегмент, день календаря, вкладка.
  static void tap() {
    if (_on) HapticFeedback.selectionClick();
  }

  /// Действие состоялось.
  static void commit() {
    if (_on) HapticFeedback.lightImpact();
  }

  /// Событие, которое стоит отметить.
  static void celebrate() {
    if (_on) HapticFeedback.mediumImpact();
  }

  /// Удаление, отмена, ошибка.
  static void warn() {
    if (_on) HapticFeedback.heavyImpact();
  }
}
