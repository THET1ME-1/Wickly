/// Отметка «в фон ушли не мы, а системное окно, которое мы сами позвали».
///
/// Дневник запирается при уходе в фон — это правильно, когда человек свернул
/// приложение. Но в фон его уводят и системные окна: запрос доступа к
/// микрофону, камера, выбор файла. Без этой отметки диктофон открывал запрос
/// разрешения, приложение запиралось, и на возврате человек видел замок вместо
/// своего листа. Выглядело как «голос не работает».
///
/// Компромисс осознанный: пока открыто наше системное окно, замок не
/// срабатывает. Экран при этом занят системным окном, а не дневником, так что
/// записи всё равно не видно.
class SystemPause {
  const SystemPause._();

  static int _depth = 0;
  static DateTime? _endedAt;

  /// Сколько ещё держим отметку после того, как окно закрылось: `resumed`
  /// прилетает не мгновенно, и снять её ровно по возврату — значит не снять.
  static const _grace = Duration(milliseconds: 1500);

  /// Идёт наше системное окно или только что закрылось.
  static bool get active {
    if (_depth > 0) return true;
    final ended = _endedAt;
    return ended != null && DateTime.now().difference(ended) < _grace;
  }

  /// Оборачивает вызов, который уводит приложение в фон.
  ///
  /// Отпускаем по отметке времени, а не отложенным таймером: висящий таймер
  /// переживает виджет и роняет тесты жалобой на незавершённое время.
  static Future<T> shield<T>(Future<T> Function() action) async {
    _depth++;
    try {
      return await action();
    } finally {
      if (_depth > 0) _depth--;
      _endedAt = DateTime.now();
    }
  }

  /// Сбрасывает отметку — на случай, если окно закрылось не по-людски.
  static void reset() {
    _depth = 0;
    _endedAt = null;
  }
}
